#include<stdio.h>

void main()
{
	int no,i,prime;
	
	printf("\n Enter any number : ");
	scanf("%d",&no);
	
	for(i=no;i>=1;i--)
	{
		prime=(no%2!=0);	
		
	}
	
	if(no=prime)
	{	
	printf("\n it is a prime number");
	}
	else
	{
	printf("\n it is not a prime number");
	}
	
	
	
}
